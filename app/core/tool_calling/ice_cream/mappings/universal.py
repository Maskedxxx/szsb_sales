# ===================== ФУНКЦИИ ДЛЯ ПОИСКА ПАТТЕРНОВ =====================

def get_universal_patterns(enum_value: str) -> List[str]:
    """
    Возвращает паттерны поиска для enum значения из ice cream маппинга.
    
    Args:
        enum_value: Значение enum из ice_cream_enum_mapping.py
        
    Returns:
        Список паттернов для поиска в реальных данных
    """
    patterns = ICE_CREAM_SPECIFIC_MAPPING.get(enum_value, [])
    
    # Если паттерны не найдены, возвращаем исходное значение как fallback
    if not patterns:
        return [enum_value]
    
    return patterns


def has_universal_enum_match(text: str, enum_value: str, field_key: str = None) -> bool:
    """
    Универсальная проверка совпадения enum значения с текстом для ice cream.
    
    Args:
        text: Текст для поиска (name, category, usage и т.д.)
        enum_value: Значение enum
        field_key: Ключ поля для специальной обработки
        
    Returns:
        True если найдено совпадение, False иначе
    """
    if not text or not enum_value:
        return False
    
    # Специальная обработка для поля usage (содержит массивы)
    if field_key == "usage":
        return check_usage_array_match(text, enum_value)
    
    # Обычный текстовый поиск для name и category
    patterns = get_universal_patterns(enum_value)
    text_lower = text.lower()
    
    # Ищем совпадения по всем паттернам
    for pattern in patterns:
        if pattern.lower() in text_lower:
            return True
    
    return False


# ===================== СПЕЦИАЛЬНЫЕ ОБРАБОТЧИКИ =====================

def check_usage_array_match(usage_array_str: str, enum_value: str) -> bool:
    """
    Специальная проверка для поля usage, которое содержит массивы строк.
    
    Args:
        usage_array_str: Строка с массивом usage (например: "['plombir', 'lakomka']")
        enum_value: Enum значение для поиска
        
    Returns:
        True если найдено совпадение в массиве, False иначе
    """
    if not usage_array_str or not enum_value:
        return False
    
    # Убираем кавычки и скобки, разделяем по запятым
    cleaned = usage_array_str.strip("[]'\"").replace("'", "").replace('"', '')
    usage_items = [item.strip() for item in cleaned.split(',')]
    
    # Получаем паттерны для enum
    patterns = get_universal_patterns(enum_value)
    
    # Проверяем каждый элемент usage массива
    for usage_item in usage_items:
        usage_lower = usage_item.lower()
        for pattern in patterns:
            if pattern.lower() in usage_lower:
                return True
    
    return False

