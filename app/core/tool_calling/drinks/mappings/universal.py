# ===================== СПЕЦИАЛЬНЫЕ REGEX ФУНКЦИИ ДЛЯ ЧИСЛОВОЙ ФИЛЬТРАЦИИ =====================

def check_dosage_range(text: str, min_val: float, max_val: float) -> bool:
    """
    Проверяет попадание дозировки в диапазон для различных единиц измерения.
    
    Args:
        text: Текст с информацией о дозировке
        min_val: Минимальное значение диапазона
        max_val: Максимальное значение диапазона
        
    Returns:
        True если дозировка в диапазоне, False иначе
    """
    # Проверяем кг/т: 0,01-3,0 кг/т
    kg_t_match = DOSAGE_KG_T_REGEX.search(text)
    if kg_t_match:
        try:
            val1 = float(kg_t_match.group(1).replace(',', '.'))
            val2_str = kg_t_match.group(2)
            if val2_str:
                val2 = float(val2_str.replace(',', '.'))
                # Проверяем попадание любого из значений диапазона
                return (min_val <= val1 <= max_val) or (min_val <= val2 <= max_val)
            else:
                return min_val <= val1 <= max_val
        except (ValueError, AttributeError):
            pass
    
    # Проверяем кг/1000л: 0,05-1,0 кг/1000 л
    kg_1000l_match = DOSAGE_KG_1000L_REGEX.search(text)
    if kg_1000l_match:
        try:
            val1 = float(kg_1000l_match.group(1).replace(',', '.'))
            val2_str = kg_1000l_match.group(2)
            if val2_str:
                val2 = float(val2_str.replace(',', '.'))
                return (min_val <= val1 <= max_val) or (min_val <= val2 <= max_val)
            else:
                return min_val <= val1 <= max_val
        except (ValueError, AttributeError):
            pass
    
    # Проверяем процентные дозировки
    pct_match = DOSAGE_PERCENTAGE_REGEX.search(text)
    if pct_match:
        try:
            val1 = float(pct_match.group(1).replace(',', '.'))
            val2_str = pct_match.group(2)
            if val2_str:
                val2 = float(val2_str.replace(',', '.'))
                return (min_val <= val1 <= max_val) or (min_val <= val2 <= max_val)
            else:
                return min_val <= val1 <= max_val
        except (ValueError, AttributeError):
            pass
    
    return False


def check_sweetness_coefficient(text: str, min_coeff: int, max_coeff: int) -> bool:
    """
    Проверяет попадание коэффициента сладости в диапазон.
    
    Args:
        text: Текст с коэффициентом сладости
        min_coeff: Минимальный коэффициент
        max_coeff: Максимальный коэффициент
        
    Returns:
        True если коэффициент в диапазоне, False иначе
    """
    # Ищем числовые значения в тексте коэффициента сладости
    coeff_match = SWEETNESS_COEFFICIENT_REGEX.findall(text)
    if coeff_match:
        try:
            for match in coeff_match:
                coefficient = int(match)
                if min_coeff <= coefficient <= max_coeff:
                    return True
        except (ValueError, AttributeError):
            pass
    
    # Специальная обработка описательных коэффициентов
    if "9% раствора сахарозы" in text.lower():
        # Эквивалентно примерно коэффициенту 280
        return min_coeff <= 280 <= max_coeff
    
    return False


def check_recipe_variant(text: str, variant_type: str) -> bool:
    """
    Проверяет тип варианта рецепта (на сахаре vs на подсластителе).
    
    Args:
        text: Текст с описанием варианта
        variant_type: Тип варианта для поиска
        
    Returns:
        True если тип соответствует, False иначе
    """
    text_lower = text.lower()
    
    if variant_type == "на_сахаре":
        return "сахар" in text_lower and "подсластитель" not in text_lower
    elif variant_type == "на_подсластителе":
        return "подсластитель" in text_lower or "сладин" in text_lower or "diet" in text_lower
    elif variant_type == "смешанный":
        return "сахар" in text_lower and "подсластитель" in text_lower
    elif variant_type == "двухстадийный":
        return "сироп" in text_lower and ("разбавление" in text_lower or "этап" in text_lower)
    
    return False


def extract_e_codes(text: str) -> List[str]:
    """
    Извлекает E-коды из текста состава.
    
    Args:
        text: Текст состава продукта
        
    Returns:
        Список найденных E-кодов
    """
    e_codes = []
    matches = E_CODE_REGEX.findall(text)
    for match in matches:
        e_codes.append(f"E{match}")
    return e_codes


def has_e_code(text: str, target_e_code: str) -> bool:
    """
    Проверяет наличие конкретного E-кода в тексте.
    
    Args:
        text: Текст для поиска
        target_e_code: Целевой E-код (например, "E102")
        
    Returns:
        True если E-код найден, False иначе
    """
    found_codes = extract_e_codes(text)
    return target_e_code.upper() in [code.upper() for code in found_codes]


