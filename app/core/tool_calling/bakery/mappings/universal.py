def matches_gost_compliance(value: Union[str, bool, None], enum_value: str) -> bool:
    """Evaluate ГОСТ compliance boolean flags."""

    normalized: Optional[bool]

    if isinstance(value, bool):
        normalized = value
    elif value is None:
        normalized = None
    elif isinstance(value, (int, float)):
        normalized = bool(value)
    else:
        text = str(value).strip().lower()
        if text in {"true", "1", "yes", "да", "gost", "соответствует"}:
            normalized = True
        elif text in {"false", "0", "no", "нет", "не соответствует"}:
            normalized = False
        else:
            normalized = None

    if enum_value == "соответствует_ГОСТ":
        return normalized is True
    if enum_value == "не_соответствует_ГОСТ":
        return normalized is False

    return False


