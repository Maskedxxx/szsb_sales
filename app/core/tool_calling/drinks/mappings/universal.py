def get_universal_patterns(enum_value: str) -> List[str]:
    """
    Возвращает паттерны поиска для enum значения из DRINKS_SPECIFIC_MAPPING.
    
    Args:
        enum_value: Значение enum из drinks enum_mapping.py
        
    Returns:
        Список паттернов для поиска в реальных данных напитков
    """
    patterns = DRINKS_SPECIFIC_MAPPING.get(enum_value, [])
    
    # Если паттерны не найдены, возвращаем исходное значение как fallback
    if not patterns:
        return [enum_value]
    
    # Добавляем вариации для русских слов (верхний/нижний регистр)
    variations = []
    for pattern in patterns:
        variations.append(pattern)
        variations.append(pattern.upper())
        variations.append(pattern.lower())
        variations.append(pattern.capitalize())
    
    # Возвращаем уникальные паттерны
    return list(set(variations))


def has_universal_enum_match(text: str, enum_value: str, field_key: str = None) -> bool:
    """
    Универсальная проверка совпадения enum значения с текстом для drinks отрасли.
    
    Args:
        text: Текст для поиска
        enum_value: Значение enum
        field_key: Ключ поля для специальной обработки
        
    Returns:
        True если найдено совпадение, False иначе
    """
    if not text or not enum_value:
        return False
    
    # Специальная обработка коэффициентов сладости
    if field_key == "sweetness_coefficient":
        return _check_sweetness_coefficient_field(text, enum_value)
    
    # Специальная обработка дозировок
    if field_key in ["dosage"]:
        return _check_dosage_field(text, enum_value, field_key)
        
    # Специальная обработка вариантов рецептов
    if field_key == "variants":
        return _check_recipe_variants_field(text, enum_value)
    
    # Специальная обработка E-кодов в составе
    if field_key == "composition" and enum_value.startswith(("E", "Е")):
        return has_e_code(text, enum_value)
    
    # Обычная текстовая проверка
    patterns = get_universal_patterns(enum_value)
    text_lower = text.lower()
    
    for pattern in patterns:
        if pattern.lower() in text_lower:
            return True
    
    return False


