# Regex для процентных диапазонов фосфатов
PHOSPHATE_PERCENTAGE_REGEX = re.compile(r"(\\d+(?:,\\d+)?(?:-\\d+(?:,\\d+)?)?)\s*%", re.IGNORECASE)

