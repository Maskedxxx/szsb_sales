def get_milk_enum_values_for_file_key(file_name: str, key_name: str) -> Dict[str, List[str]]:
    """
    Возвращает enum значения для конкретного ключа в конкретном файле.
    
    Args:
        file_name: Имя файла (например, "milk_protein.json")
        key_name: Имя ключа (например, "name", "properties")
        
    Returns:
        Словарь с enum категориями и значениями для данного ключа
    """
    file_data = MILK_ENUM_MAPPING.get(file_name, {})
    return file_data.get(key_name, {})


def get_all_milk_enums_for_file(file_name: str) -> Dict[str, Dict[str, List[str]]]:
    """
    Возвращает все enum значения для всех ключей конкретного файла.
    
    Args:
        file_name: Имя файла (например, "milk_protein.json")
        
    Returns:
        Полный словарь с enum'ами для всех ключей файла
    """
    return MILK_ENUM_MAPPING.get(file_name, {})


