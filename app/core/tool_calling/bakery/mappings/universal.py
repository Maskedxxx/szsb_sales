def _parse_decimal(value: Union[str, float, int, None]) -> Optional[float]:
    """Convert raw JSON value into a float for numeric comparisons."""

    if value is None:
        return None

    if isinstance(value, (int, float)):
        return float(value)

    if isinstance(value, str):
        candidate = value.replace(" ", "").replace(",", ".")
        try:
            return float(candidate)
        except ValueError:
            return None

    return None


def matches_grinding_degree(value: Union[str, float, int, None], enum_value: str) -> bool:
    """Check if a grinding degree value belongs to the requested enum band."""

    numeric = _parse_decimal(value)

    if enum_value == "значение_не_указано":
        return numeric is None

    if numeric is None:
        return False

    band = GRINDING_RANGES.get(enum_value)
    if band is None:
        return False

    lower, upper = band

    if lower is not None and numeric < lower:
        return False
    if upper is not None and numeric > upper:
        return False

    return True


def _parse_range(value: Union[str, float, int, None]) -> Optional[float]:
    """Return average numeric value for a range expressed as string."""

    if value is None:
        return None

    if isinstance(value, (int, float)):
        return float(value)

    if not isinstance(value, str):
        return None

    cleaned = value.replace("\u2013", "-").replace("\u2014", "-")
    segments = []
    for part in cleaned.split('-'):
        part = part.strip().split(" ")[0]
        try:
            segments.append(float(part.replace(",", ".")))
        except ValueError:
            continue

    if not segments:
        return None

    if len(segments) == 1:
        return segments[0]

    return sum(segments) / len(segments)


def _extract_numeric_bounds(value: Union[str, float, int, None]) -> Optional[Tuple[float, float]]:
    """Extract minimum and maximum numeric values from free-form dosage text."""

    if value is None:
        return None

    if isinstance(value, (int, float)):
        numeric = float(value)
        return numeric, numeric

    if not isinstance(value, str):
        return None

    numbers = [float(token.replace(',', '.')) for token in re.findall(r"\d+(?:[.,]\d+)?", value)]
    if not numbers:
        return None

    return min(numbers), max(numbers)


def matches_viscosity_band(value: Union[str, float, int, None], enum_value: str) -> bool:
    """Classify viscosity textual ranges into enum buckets."""

    if enum_value == "нет_данных":
        return value in {None, "-", "—", "нет данных", ""}

    if enum_value == "измерена_секундомером":
        if isinstance(value, str):
            return "сек" in value.lower()
        return False

    reference = _parse_range(value)
    if reference is None:
        return False

    band = VISCOSITY_RANGES.get(enum_value)
    if band is None:
        return False

    lower, upper = band

    if lower is not None and reference < lower:
        return False
    if upper is not None and reference > upper:
        return False

    return True


def _match_numeric_band(
    value: Union[str, float, int, None],
    enum_value: str,
    bands: Dict[str, Tuple[Optional[float], Optional[float]]],
    *,
    percent_required: bool = False,
) -> bool:
    bounds = _extract_numeric_bounds(value)
    if bounds is None:
        return False

    observed_min, observed_max = bounds
    target = bands.get(enum_value)
    if target is None:
        return False

    expected_min, expected_max = target

    if expected_min is not None and observed_max < expected_min:
        return False
    if expected_max is not None and observed_min > expected_max:
        return False

    if percent_required and isinstance(value, str) and '%' not in value:
        return False

    return True


